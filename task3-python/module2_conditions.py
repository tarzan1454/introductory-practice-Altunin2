# =====================================================
# Упражнение 1: Результат периода (прибыль / убыток)
# =====================================================
print("\n1. Результат периода")
profit = float(input("Введите прибыль за месяц (руб.): "))

if profit > 0:
    print("Результат: ПРИБЫЛЬ ✅")
elif profit < 0:
    print("Результат: УБЫТОК ❌")
else:
    print("Результат: БЕЗУБЫТОЧНОСТЬ ➖")
print()

# =====================================================
# Упражнение 2: Категория бизнеса по выручке
# =====================================================
print("2. Категория бизнеса")
revenue = float(input("Введите годовую выручку (млн руб.): "))

if revenue < 1:
    category = "Микробизнес"
elif revenue < 10:
    category = "Малый бизнес"
elif revenue < 100:
    category = "Средний бизнес"
else:
    category = "Крупный бизнес"

print(f"Категория: {category}")
print()

# =====================================================
# Упражнение 3: Расчёт НДФЛ (13% или 15%)
# =====================================================
print("3. Расчёт НДФЛ")
salary = float(input("Введите зарплату (руб.): "))

if salary <= 50000:
    tax_rate = 13
else:
    tax_rate = 15

tax = salary * (tax_rate / 100)
salary_hand = salary - tax

print(f"Ставка НДФЛ: {tax_rate}%")
print(f"Сумма налога: {tax:.2f} руб.")
print(f"Зарплата на руки: {salary_hand:.2f} руб.")
print()

# =====================================================
# Упражнение 4: Таблица умножения процентной ставки
# =====================================================
print("4. Таблица умножения ставки")
interest_rate = float(input("Введите процентную ставку (%): "))

print(f"\nТаблица умножения ставки {interest_rate}%:")
for i in range(1, 13):
    result = interest_rate * i
    print(f"{interest_rate}% × {i:2} = {result:6.2f}%")
print()

# =====================================================
# Упражнение 5: Анализ цен (дороже 300 руб.)
# =====================================================
print("5. Анализ цен")
prices = [250, 320, 180, 450, 299, 400, 150, 350]

print("Список цен:", prices)
print("\nАнализ:")

for price in prices:
    if price > 300:
        print(f"  {price} руб. → ДОРОГО 💰")
    else:
        print(f"  {price} руб. → нормально ✅")

